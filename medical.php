      <h3>Breathing and Respiratory Failure</h3>
      <p>The term, &quot;respiratory failure,&quot; is used when the lungs are<br>
        unable to perform their basic task - gas exchange. This<br>
        process involves transfer of oxygen from inhaled air into<br>
        the blood and of carbon dioxide from the blood into the<br>
        lungs, with the result that the arterial blood, blood<br>
        circulating through the body from the heart, has enough<br>
        oxygen to nourish the tissues.</p>
      <p>Gas exchange occurs in tiny air sacs in the lung, called<br>
        alveoli. When a person breathes in (inspiration), air is<br>
        brought into the alveoli by the action of the respiratory<br>
        muscles - the diaphragm, the muscles between the ribs, and<br>
        the accessory muscles (those between the neck and the chest<br>
        wall). These are collectively called &quot;the ventilatory<br>
        apparatus.&quot; The activity of the respiratory muscles is<br>
        controlled by respiratory centers in the brain. The brain's<br>
        respiratory centers in turn are controlled by<br>
        chemoreceptors, special cells that are sensitive to the<br>
        amounts of carbon dioxide or oxygen in the blood. The<br>
        chemoreceptors that are sensitive to oxygen concentration<br>
        are located in the large arteries in the neck in the carotid<br>
        bodies. When they sense a fall in the level of oxygen in the<br>
        blood, they send messages that stimulate the respiratory<br>
        center in the brain so that there will be an increase in the<br>
        rate or depth of breathing.</p>
      <p>Whenever any part of the ventilatory apparatus and/or the<br>
        respiratory centers fails to work properly, the result can<br>
        be respiratory failure. Both adults and babies can develop<br>
        respiratory failure. In infants, however, respiratory<br>
        failure occurs mostly in premature babies whose lungs have<br>
        not yet fully developed.</p>
      <h3>What Happens During Respiratory Failure</h3>
      <p>When the process of gas exchange is faulty, there is not<br>
        enough oxygen in the blood (hypoxemia) to fuel the body's<br>
        metabolic activity. In addition, sometimes there is also an<br>
        accumulation of carbon dioxide, a waste product of<br>
        metabolism, in the blood and tissues (hypercapnia).<br>
        Hypercapnia makes blood more acidic; this condition is<br>
        called acidemia. Eventually the body tissues become acidic.<br>
        This condition, called acidosis, injures the body's cells<br>
        and interferes with the functions of the heart and central<br>
        nervous system. Ultimately, lack of oxygen in the blood<br>
        causes death of the cells in the brain and other tissues. If<br>
        not adequately treated, respiratory failure is fatal. </p>
      <p>People whose normal lungs have been injured, such as from<br>
        exposure to noxious gases, steam, or heat during a fire, can<br>
        subsequently go into respiratory failure. Adult respiratory<br>
        distress syndrome (ARDS), also referred to as acute<br>
        respiratory distress syndrome, is a form of acute<br>
        respiratory failure caused by extensive lung injury<br>
        following a variety of catastrophic events such as shock,<br>
        severe infection, and burns. ARDS can occur in individuals<br>
        with or without previous lung disease. </p>
      <h3>Management Of Respiratory Failure</h3>
      <p>The patient with respiratory failure cannot be adequately<br>
        treated in the general care areas of the hospital.<br>
        Therefore, patients in severe respiratory failure are<br>
        usually treated in the intensive care unit. Current therapy<br>
        for all forms of respiratory failure attempts, first, to<br>
        provide support for the heart, lungs, and other affected<br>
        vital organs; and second, to identify and treat the<br>
        underlying cause. </p>
      <p>Since the immediate threat to patients with respiratory<br>
        failure is due to the inadequate level of oxygen delivered<br>
        to the tissues, oxygenation is the basic therapy for acute<br>
        respiratory failure due to lung disease. Oxygen-enriched air<br>
        is usually given to the patient by nasal prongs, oxygen<br>
        mask, or by placing an airtube into the trachea (windpipe).<br>
        Since prolonged high oxygen levels can be toxic, the<br>
        concentration of oxygen must be carefully controlled for<br>
        both short- and long-term treatment. Assisted ventilation<br>
        with mechanical devices may be the first priority for<br>
        neuromuscular disease patients going into respiratory<br>
        failure. Additional treatments employ ventilation which<br>
        helps to keep the lungs inflated at low lung volumes<br>
        (positive end-expiratory pressure, PEEP), and fluid and<br>
        nutritional management. </p>
      <h3>Mechanical Ventilation </h3>
      <p>If the patient is tiring despite ongoing therapy, a<br>
        mechanical ventilator, also called a respirator, is used.<br>
        The ventilator assists or controls the patient's breathing.</p>
      <h3>Weaning the Patients From Ventilators </h3>
      <p>The process of returning the patient to unassisted and<br>
        spontaneous breathing is called weaning. Weaning is a<br>
        complex process that requires the understanding and<br>
        cooperation of the patient. It can cause great fatigue and<br>
        depression in patients because of the slow- and long-term<br>
        nature of the treatment procedures. </p>
      <p>Weaning a patient too rapidly or prematurely can be<br>
        dangerous. Some patients, particularly those who had severe<br>
        underlying cardiac disease and prolonged episodes of acute<br>
        illnesses, may require weeks to months to wean. The doctor<br>
        considers weaning only when the patient is awake, has good<br>
        nutrition, and is able to cough and breathe deeply.</p></td>
